interface Emission {
    transportCarbon: number,
    energyCarbon: number,
    waterCarbon: number
}

export const getRecomendation = ({transportCarbon, energyCarbon, waterCarbon}: Emission) =>{
    if (transportCarbon >= energyCarbon && transportCarbon >= waterCarbon) {
        return { title: "транспортного углеродного следа",
            regs : [
                "Рассмотрите возможность использования каршеринга или общественного транспорта хотя бы 1-2 раза в неделю.",
                "Попробуйте совместные поездки (например, с коллегами или через сервисы поиска попутчиков).",
                "Регулярно проверяйте давление в шинах – это поможет снизить расход топлива до 5%.",
                "Старайтесь избегать резкого торможения и ускорения – это может снизить расход топлива до 20%.",
                "Если есть возможность, рассмотрите переход на гибридный или электрический автомобиль."
            ]
        };
    } else if (energyCarbon >= transportCarbon && energyCarbon >= waterCarbon) {
        return {
            title : "потребления природного газа",
            regs : [
                "Если в холодное время года у вас высокий расход газа:",
                "Утеплите окна и двери – это поможет сократить потери тепла до 30%.",
                "Рассмотрите установку программируемого термостата – он позволит снизить расход газа на 10-15%.",
                "Попробуйте понизить температуру на 1-2°C ночью – это практически не ощущается, но значительно экономит энергию.",
                "Используйте электропростынь или теплое одеяло вместо обогревателя.",
                "Если вы готовите на газе:",
                "Используйте крышку на кастрюлю – это ускоряет процесс кипячения и снижает расход газа.",
                "Старайтесь готовить на среднем огне – на максимальном режиме расход газа выше, а разница в скорости приготовления минимальна.",
                "Регулярно проверяйте газовое оборудование на утечки – при появлении запаха газа обязательно вызывайте специалиста."
            ]
        };
    } else {
        return {
            title : "потребления электроэнергий",
            regs : [
                "Рассмотрите замену обычных ламп на LED – это позволит сократить энергопотребление до 80%.",
                "Выключайте устройства из розетки (например, зарядные устройства и телевизоры), так как они продолжают потреблять энергию даже в режиме ожидания.",
                "Используйте умные розетки – они автоматически отключают технику, когда она не используется.",
                "Стирайте в холодной воде и загружайте стиральную машину полностью – это помогает сэкономить до 90% электроэнергии.",
                "Подумайте об установке энергоэффективных окон или использовании плотных штор для лучшего сохранения тепла.",
                "Если вы пользуетесь кондиционером:",
                "Установите температуру в диапазоне 24-26°C – это наиболее энергоэффективный режим.",
                "Регулярно очищайте фильтры – загрязненные фильтры увеличивают расход энергии на 5-10%.",
                "При умеренной температуре используйте вентилятор вместо кондиционера, если это возможно."
            ]
        };
    }
}